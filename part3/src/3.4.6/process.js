console.log(process.version);   // 노드 버전
console.log(process.arch);      // 프로세서 아키텍처 정보
console.log(process.platform);  // 운영체제 플랫폼
console.log(process.pid);       // 프로세스 id
console.log(process.uptime());  // 프로세스 시작 후 흐른 시간
console.log(process.execPath);  // 노드 경로
console.log(process.cwd());   // 프로세스 실행 위치
console.log(process.cpuUsage());    // cpu 사용량